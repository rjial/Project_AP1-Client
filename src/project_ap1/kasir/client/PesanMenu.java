    private void update_makanan() {
        try {
            idMenu_selected = null;
            String sqlmakanan = "SELECT * FROM menu INNER JOIN makanan ON makanan.ID_MENU = menu.ID_MENU";
//            String output = "select * from menu, makanan, minuman";
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(sqlmakanan);
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            int q = stData.getColumnCount();
            DefaultTableModel tb_list_makanan = (DefaultTableModel) tblMenuMakanan.getModel();
            tb_list_makanan.setRowCount(0);
            daftarMenu.clear();

            while (rs.next()) {
                Vector columndata = new Vector();
                for (int i = 1; i < q; i++) {
                   daftarMenu.put(rs.getString("ID_MENU"), new Menu(rs.getString("ID_MENU"), rs.getString("NAMA_MAKANAN"), rs.getInt("STOCK"), rs.getInt("HARGA"), rs.getBinaryStream("GAMBAR_MENU"), 0, 0));
//                    columndata.add(rs.getString("STOCK"));
//                    columndata.add(rs.getString("NAMA_MAKANAN"));
                    columndata.add(rs.getString("ID_MENU"));
                    columndata.add(rs.getString("NAMA_MAKANAN"));
                    columndata.add(rs.getString("STOCK"));
                    columndata.add(rs.getString("HARGA"));
                }
                tb_list_makanan.addRow(columndata);
            }
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
    private void update_minuman() {
        String sqlminuman = "SELECT * FROM menu INNER JOIN minuman ON minuman.ID_MENU = menu.ID_MENU";
//            String output = "select * from menu, makanan, minuman";
        try {
            
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(sqlminuman);
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            int q = stData.getColumnCount();
            DefaultTableModel tb_list_minuman = (DefaultTableModel) tblMenuMinuman.getModel();
            tb_list_minuman.setRowCount(0);
            daftarMenuMinuman.clear();

            while (rs.next()) {
                Vector columndata = new Vector();
                for (int i = 1; i < q; i++) {
                    daftarMenuMinuman.put(rs.getString("ID_MENU"), new Menu(rs.getString("ID_MENU"), rs.getString("NAMA_MINUMAN"), rs.getInt("STOCK"), rs.getInt("HARGA"), rs.getBinaryStream("GAMBAR_MENU"), 1, 0));
//                    columndata.add(rs.getString("STOCK"));
//                    columndata.add(rs.getString("NAMA_MAKANAN"));
                    columndata.add(rs.getString("ID_MENU"));
                    columndata.add(rs.getString("NAMA_MINUMAN"));
                    columndata.add(rs.getString("STOCK"));
                    columndata.add(rs.getString("HARGA"));
                }
                tb_list_minuman.addRow(columndata);
            }
        } catch (SQLException ex) {
            Logger.getLogger(PesanMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        

        
    }
